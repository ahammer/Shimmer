package com.adamhammer.shimmer.samples.dnd.model

object GameRules {
    const val SHARED_RULEBOOK = """
# D&D Campaign Shared Rulebook

## 1. Narrative Pacing & Movement
- **Do Not Linger:** The story must keep moving. The party should not spend more than 2-3 rounds in a single location unless engaged in a major combat encounter.
- **Seek Exits:** Players should actively look for exits, travel to new areas, and pursue quests rather than just chatting or drinking at a tavern.
- **Force Transitions:** If the party dawdles, the Dungeon Master MUST provide clear exits, introduce urgent plot hooks, or forcefully transition the scene to a new location.

## 2. Player Agency & Action
- **Be Proactive:** Players must take decisive actions that advance the plot.
- **Stay in Character:** Actions should reflect the character's class, background, and current emotional state.
- **Coordinate:** Use whispers to coordinate with other party members when necessary.

## 3. Dungeon Master Responsibilities
- **Drive the Plot:** The DM is responsible for maintaining narrative momentum.
- **Provide Options:** Always present clear, actionable choices and obvious exits to the players.
- **Escalate Tension:** If players ignore plot hooks, escalate the situation. Introduce sudden events, attacks, or urgent news that forces them to react and move.
- **Enforce Pacing:** Monitor how long the party has been in one place. If they overstay, use the environment or NPCs to push them out.
"""
}
